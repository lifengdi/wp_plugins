DROP TABLE IF EXISTS `wp_history_today`;
CREATE TABLE `wp_history_today`
(
    `id`             int UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '主键ID',
    `month_day`      varchar(4)  NOT NULL COMMENT '日期（MMDD格式，如0101）',
    `event_type`     varchar(2)  NOT NULL COMMENT '事件类型：中/外',
    `event_year`     varchar(10) NOT NULL COMMENT '事件年份（支持古代年份如618）',
    `event_content`  text        NOT NULL COMMENT '事件详细描述',
    `event_category` varchar(20) DEFAULT '' COMMENT '事件分类：政治/科技/战争/文化/体育/民生',
    `created_at`     datetime    DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    `updated_at`     datetime    DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    PRIMARY KEY (`id`),
    KEY              `idx_month_day` (`month_day`) COMMENT '按日期索引，提升查询速度'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='历史上的今天事件表';


